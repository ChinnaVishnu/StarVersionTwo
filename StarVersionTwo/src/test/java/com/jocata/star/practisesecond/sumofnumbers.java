package com.jocata.star.practisesecond;

public class sumofnumbers {

	
	public static void printSums() {
	    int sum = 0;
	    for (int i = 1; i <= 10; i++) {
	        sum += i;
	        System.out.println(sum);
	    }
	}
	
	public static void main(String[] args) {
		
		sumofnumbers.printSums();
		
	}
	
}
